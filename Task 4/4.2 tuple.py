# Tuples
my_tuple = ("apple", "banana", "cherry")

# tuples are immutable, we cannot change their elements once they are created.
# we cannot add elements to a tuple once it is created.
# we cannot remove elements from a tuple once it is created.

# Sorting a tuple using the sorted() function
my_tuple = (3, 2, 1)
sorted_tuple = sorted(my_tuple)
print(sorted_tuple)  # Output: [1, 2, 3]

# Indexing
my_tuple = ("apple", "banana", "cherry")
print(my_tuple[1])  # Output: "banana"